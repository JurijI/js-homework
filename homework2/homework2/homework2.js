// ### Завдання 2: Перевірка віку
// Напишіть програму, яка приймає вік користувача і виводить:
// - "Дитина", якщо вік менше 12 років.
// - "Підліток", якщо вік від 12 до 18 років.
// - "Дорослий", якщо вік від 18 до 60 років.
// - "Пенсіонер", якщо вік більше 60 років.
let age = parseInt(prompt("Введіть ваш вік:"));

if (age < 12) {
  document.write("Дитина");
} else if (age >= 12 && age <= 18) {
  document.write("Підліток");
} else if (age >= 18 && age <= 60) {
  document.write("Дорослий");
} else if(age > 60){
  document.write("Пенсіонер");
} else {
  document.write("Ви ввели некоректний вік.");
}
document.write("<br><br>");

// ### Завдання 3: Таблиця множення
// Напишіть програму, яка виводить таблицю множення для чисел від 
// 1 до 10.
//  Використовуйте вкладені цикли.
for (let i = 1; i <= 10; i++) {
    for (let j = 1; j <= 10; j++) {
      document.write(i + " * " + j + " = " + (i * j) + "<br>");
    }
    document.write("<br><br>"); 

}
