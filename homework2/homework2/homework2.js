// ### Завдання 4: Сума чисел
// Напишіть програму, яка обчислює суму всіх чисел від 1 до заданого 
// користувачем числа. Наприклад, якщо користувач введе 5, 
// програма має вивести 15, оскільки 1+2+3+4+5 = 15.
let number = parseInt(prompt("Введіть число:"));
let sum = 0;

for (let i = 1; i <= number; i++) {
  sum += i;
}

document.write("Сума чисел:  1 + " + number + " = " + sum);
document.write("<br>"); 

