
// ### Завдання 5: Трикутник
// Напишіть програму, яка малює прямокутний трикутник з зірочок (`*`). Висота трикутника повинна бути задана користувачем.
// ```javascript
// let height = parseInt(prompt("Введіть висоту трикутника:"));
// for (let i = 1; i <= height; i++) {
//     console.log('*'.repeat(i));
