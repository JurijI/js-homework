let height_1 = parseInt(prompt("Введіть висоту рівнобедреного трикутника:"));
for (let i = 1; i <= height_1; i++) {
    document.write("&nbsp;&nbsp;".repeat(height_1 - i) + '*'.repeat(2 * i - 1) + '<br>');
}

